# 7.	Write a Python program to get the difference between a given number and 17,
# if the number is greater than 17 return double the absolute difference.

# Distance is used to calculate the difference between two different numbers
a = int(input("Input number here: "))
if distance(a, 17) > a:
    abs(a)
print(a)
# abs(_) finds the absolute value of the integer
