# 9.	Write a Python program to find the repeated items of a tuple.
# Create the tuple, then print it to make sure it works
tpl1 = [9, 1, 2, 3, 4, 9, 8, 7, 6, 5, 9]
print(tpl1)
count1 = tpl1.count(9)
# Turn count into a printable object. The 9 is the number it is counting.
print("The number nine is shown", count1, "times.")
